// Inicializa o modo gráfico. assumimos que o bootloader setou VESA 0x101.
// aqui só desenhamos um pequeno teste; evite loops grandes para não travar.
void graphics_init() {
    graphics_clear(COLOR_BLACK);

    // barras coloridas pequenas para teste
    for (int x = 0; x < 50; x++) {
        for (int y = 0; y < 20; y++) {
            graphics_put_pixel(x, y, COLOR_RED);
        }
    }
    for (int x = 50; x < 100; x++) {
        for (int y = 0; y < 20; y++) {
            graphics_put_pixel(x, y, COLOR_GREEN);
        }
    }
    for (int x = 100; x < 150; x++) {
        for (int y = 0; y < 20; y++) {
            graphics_put_pixel(x, y, COLOR_BLUE);
        }
    }
}

// Limpa a tela com uma cor sólida (convertendo para 4‑bit se
// necessário). O argumento `color` permanece uint16_t para que o
// restante do código possa continuar a passar valores 16‑bit; a
// máscara abaixo trata a redução quando estivermos em modo VGA.
void graphics_clear(uint16_t color) {
    uint8_t value = (uint8_t)(color & 0xF);
    for (int y = 0; y < GRAPHICS_HEIGHT; y++) {
        for (int x = 0; x < GRAPHICS_WIDTH; x++) {
            GRAPHICS_FRAMEBUFFER[y * GRAPHICS_WIDTH + x] = value;
        }
    }
}

// Desenha um pixel na posição (x, y)
void graphics_put_pixel(int x, int y, uint16_t color) {
    if (x >= 0 && x < GRAPHICS_WIDTH && y >= 0 && y < GRAPHICS_HEIGHT) {
        uint8_t value = (uint8_t)(color & 0xF);
        GRAPHICS_FRAMEBUFFER[y * GRAPHICS_WIDTH + x] = value;
    }
}

// Desenha um caractere na posição (x, y) com cores fg/bg
void graphics_draw_char(int x, int y, char c, uint16_t fg_color, uint16_t bg_color) {
    uint8_t* glyph = (uint8_t*)font_data[(uint8_t)c];
    for (int row = 0; row < FONT_HEIGHT; row++) {
        for (int col = 0; col < FONT_WIDTH; col++) {
            uint16_t color = (glyph[row] & (1 << (7 - col))) ? fg_color : bg_color;
            graphics_put_pixel(x + col, y + row, color);
        }
    }
}

// Desenha uma string começando em (x, y)
void graphics_draw_string(int x, int y, const char* str, uint16_t fg_color, uint16_t bg_color) {
    int start_x = x;
    while (*str) {
        if (*str == '\n') {
            y += FONT_HEIGHT;
            x = start_x;
        } else {
            graphics_draw_char(x, y, *str, fg_color, bg_color);
            x += FONT_WIDTH;
        }
        str++;
    }
}